export const setPocRate = (pr) => {
  // 숫자로 계산할 수 없으면 에러 0%를 반환.
  const c = 100 - Number(pr);
  return isNaN(c) ? "0%" : `${c}%`;
};

export const setRate = (r) => {
  //범위 초과 시 Er을 반환
  if (r === "1" || r === "2") {
    return "D";
  } else if (r === "3" || r === "4") {
    return "C";
  } else if (r === "5" || r === "6") {
    return "B";
  } else if (r === "7" || r === "8") {
    return "A";
  } else if (r === "9" || r === "10") {
    return "S";
  } else {
    return "";
  }
};

export const disCtx = (s, isSimple) => {
  if (s === "7" || s === "8") {
    return isSimple ? "다소 높음" : "확률이 다소 높음";
  } else if (s === "9" || s === "10") {
    return isSimple ? "높음" : "확률이 높음";
  } else {
    return isSimple ? "낮음" : "확률이 낮음";
  }
};

export function roundDown(numberString) {
  const number = Number(numberString);
  return Math.floor(number * Math.pow(10, 1)) / Math.pow(10, 1);
}

export function roundDown2(numberString) {
  const number = Number(numberString);
  return Math.floor(number * Math.pow(10, 2)) / Math.pow(10, 2);
}
